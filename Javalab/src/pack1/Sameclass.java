package pack1;

public class Sameclass
{
      int d;
      private int p;
      protected int pro;
      public int a;
      //Sameclass()
      public Sameclass()  //constructor
      {
    	  System.out.println("Default access specifier : "+d);
    	  System.out.println("Private access specifier : "+p);
    	  System.out.println("Protected access specifier : "+pro);
    	  System.out.println("Public access specifier : "+a);
      }
      
      
}
